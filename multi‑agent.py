"""## Communication Protocols: When Agents Stop Sharing Memory

Today, every agent in this notebook lives in the same Python process. When the LangGraph orchestrator routes from `planner` to `searcher`, a lot of things happen *for free* because of shared memory:

| What you get for free in shared memory | What you must add when crossing a network |
|----------------------------------------|-------------------------------------------|
| Caller identity (it is whoever is running the code) | A `sender` field |
| Ordering (return values match calls) | A `task_id` per request |
| Routing (LangGraph finds the next node) | A `receiver` field |
| Intent (the function does one thing) | An `intent` field |
| Error correlation (stack traces) | A `correlation_id` to link related calls |
| Payload separation (no middleware) | Envelope vs payload separation |

The moment one agent moves to another machine, **all of these implicit things must become explicit**. That is what a communication protocol is. It is not bureaucracy — it is the minimum information needed to recover, on a network, the things that were free in shared memory.

### The Envelope

A communication protocol envelope looks like this:

```python
{
    "task_id":        "...",   # so responses can be matched to requests
    "sender":         "...",   # so the receiver can authorize the call
    "receiver":       "...",   # so gateways and routers know where to send it
    "intent":         "...",   # so the agent knows which of its skills to invoke
    "payload":        {...},   # the actual application data
    "correlation_id": "...",   # so all messages from one user request can be traced together
}
```

Each field replaces something that was free in shared memory. Remove `task_id` and you cannot match responses to requests in async systems. Remove `sender` and you cannot enforce authorization. Remove `receiver` and you cannot route through a gateway. Remove `intent` and you have assumed agents do only one thing. Remove envelope/payload separation and middleware will accidentally log your secrets.

### A2A: The Accepted Standard

**A2A (Agent-to-Agent)** is the protocol for letting agents built by different teams, on different frameworks, possibly running on different machines, talk to each other. Originally proposed by Google in 2024.

In 2025, **ACP (Agent Communication Protocol)** from IBM was merged into A2A under the Linux Foundation. So today, A2A is the unified standard. ACP is folded in.

The minimum requirements to call something "A2A":

1. **Agent Card** — a JSON document at `/.well-known/agent.json` describing the agent (name, skills, endpoint).
2. **HTTP endpoint** that accepts task messages.
3. **Standard envelope** — `task_id`, `sender`, `receiver`, `intent`, `payload`.
4. **Standard response format** — result or error, tied back to the request via `task_id`.

### What this section will show

We will not implement real HTTP or real A2A. Instead, we will simulate the **shape** of message-passing in-process so the envelope concept becomes concrete. We will:

1. Define the `Message` dataclass (the envelope).
2. Define a tiny in-memory `MessageBus`.
3. Refactor *only* the Planner → Searcher interaction to go through the bus.
4. Leave the rest of the pipeline alone, so the contrast is visible.

### Do we still need the orchestrator?

Yes. A2A does not eliminate the orchestrator — it changes the orchestrator's job from *calling functions* (or routing graph nodes) to *speaking a protocol*. The taxonomy stays. The RACI stays. What changes is that "Responsible" agents can now live anywhere. Someone still has to be Accountable for the run, and that someone is still the orchestrator.
"""

import uuid
from typing import Any, Callable


@dataclass
class Message:
    """A communication envelope. The minimum information needed when agents
    do not share memory.

    Notes for students:
    - task_id: lets us match responses to requests even when calls are async.
    - sender / receiver: identity, for authorization and routing.
    - intent: which capability of the receiver are we invoking.
    - payload: the application data - kept SEPARATE from the envelope so that
               middleware (logging, auth) can read the envelope without
               touching potentially sensitive payload contents.
    - correlation_id: links every message that came from the same user request,
                      so we can reconstruct a distributed trace later.
    """
    task_id: str
    sender: str
    receiver: str
    intent: str
    payload: dict[str, Any]
    correlation_id: str


class MessageBus:
    """A tiny in-memory message bus. In a real A2A system, this would be HTTP
    calls between separate servers. Here, we keep it in-process so the
    *shape* of message passing is visible without the distraction of networking.
    """

    def __init__(self) -> None:
        self._handlers: dict[str, Callable[[Message], Message]] = {}

    def register(self, agent_name: str, handler: Callable[[Message], Message]) -> None:
        """An agent registers itself on the bus and declares how it handles
        incoming messages. In real A2A, this is the Agent Card + HTTP endpoint.
        """
        self._handlers[agent_name] = handler

    def send(self, message: Message) -> Message:
        """Deliver a message to its receiver and return the response message.

        Notes for students:
        - In a real system this would be an HTTP POST to the receiver's endpoint.
        - The response is itself a Message, with the same correlation_id, so
          the caller can match it.
        """
        if message.receiver not in self._handlers:
            raise ValueError(f"No agent registered as '{message.receiver}'")

        console.print(f"[dim]bus: {message.sender} -> {message.receiver} "
                      f"(intent={message.intent}, task_id={message.task_id[:8]}...)[/dim]")

        handler = self._handlers[message.receiver]
        return handler(message)



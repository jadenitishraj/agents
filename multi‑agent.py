"""## Human-in-the-Loop: When the Critic Is Not Enough

For most questions, the automated Critic is a good enough gate. But for **sensitive topics** — medical, legal, tax, financial — even a perfectly approved draft should not be released without a human reviewing it.

So we add a **second gate, after the Critic, before the final answer is returned**.

### How RACI shifts

In the original notebook, the Critic was always *Accountable*. That is fine for ordinary questions. For sensitive runs, accountability should rise to a human:

| Run type | Responsible | Accountable | Consulted |
|----------|-------------|-------------|-----------|
| Ordinary | Writer | **Critic** | Reader, Compliance |
| Sensitive (with HITL) | Writer | **Human** | **Critic**, Reader, Compliance |

The Critic does not disappear — it becomes *Consulted*. Its judgment still matters, but the final say is human.

This is one of the most important RACI lessons: **role assignment is not fixed; it shifts with risk**. The same agent can be Accountable on one run and Consulted on another, depending on what is at stake.

### The trigger

We trigger HITL only when `Compliance` is on the team — i.e., only when the question was already flagged as sensitive by the team selector earlier in the notebook. So the gate appears for tax, medical, legal, financial questions, but not for ordinary ones.

### The flow

1. Writer produces draft.
2. Critic reviews. If rejected → loop continues as before.
3. Critic approves → if `Compliance` is on the team, prompt the human.
4. Human approves → final answer returned.
5. Human rejects → human's reason is treated like Critic feedback, loop continues.

### Pedagogical note

In the vanilla notebook this used Python's `input()`. In LangGraph we can do this much more cleanly with the framework's built-in **`interrupt()`** primitive plus a checkpointer (`MemorySaver`). When the graph hits an `interrupt`, it pauses, the controlling code asks the human for input, and then resumes via `Command(resume=...)`. This is the same shape as the original — pause, wait for human, resume — but it is the idiomatic LangGraph way and it is exactly how a real Slack approval / webhook gate would be wired in production.

In a non-interactive environment (a notebook test runner with no stdin), we fall back to auto-approve so the demo still completes — same behaviour as the original `EOFError` fallback.
"""

from langgraph.checkpoint.memory import MemorySaver
from langgraph.types import interrupt, Command


def human_review_node(state: State) -> dict:
    """The new HITL gate. Only fires when Compliance is on the team AND the
    Critic has approved. Pauses the graph using LangGraph's interrupt() and
    waits for human input."""
    # Only run for sensitive questions. For non-sensitive runs, this node
    # is a no-op pass-through.
    if "Compliance" not in state.get("team", []):
        return {}
    if not state.get("approved", False):
        return {}

    console.print(Panel(state.get("answer", ""), title="DRAFT FOR HUMAN REVIEW", style="yellow"))
    console.print("[bold yellow]This is a sensitive topic. Human approval required.[/bold yellow]")

    # interrupt() pauses the graph. The controlling code (the cell below)
    # is responsible for asking the human and resuming with Command(resume=...).
    decision = interrupt(
        {
            "draft": state.get("answer", ""),
            "question": state["question"],
            "prompt": "Approve this draft? Reply 'y' to approve or a reason text to reject.",
        }
    )

    # decision is whatever was passed via Command(resume=...) by the caller.
    if isinstance(decision, str) and decision.strip().lower() == "y":
        console.print("[bold green]   HUMAN APPROVED - final answer accepted.[/bold green]")
        return {"approved": True}

    # Anything else is treated as a rejection reason.
    reason = decision if isinstance(decision, str) else "Human rejected without reason"
    console.print(f"[bold red]   HUMAN REJECTED: {reason}[/bold red]")
    return {
        "approved": False,
        "critic_feedback": f"Human reviewer said: {reason}",
    }


def human_router(state: State) -> str:
    """After the human review, either we are done or we go back to the writer."""
    if state.get("approved", False):
        return "end"
    if state.get("iterations", 0) >= state.get("max_iterations", MAX_ITERATIONS):
        return "end"
    return "writer"


def build_research_graph_hitl():
    """Same graph as before, but with a human_review node sitting between the
    Critic and the END for sensitive runs."""
    graph = StateGraph(State)

    graph.add_node("planner", planner_node)
    graph.add_node("searcher", searcher_node)
    graph.add_node("reader", reader_node)
    graph.add_node("compliance", compliance_node)
    graph.add_node("writer", writer_node)
    graph.add_node("critic", critic_node)
    graph.add_node("human_review", human_review_node)

    graph.add_edge(START, "planner")
    graph.add_edge("planner", "searcher")
    graph.add_edge("searcher", "reader")
    graph.add_edge("reader", "compliance")
    graph.add_edge("compliance", "writer")
    graph.add_edge("writer", "critic")

    # If Critic rejects, loop back to the Writer. If Critic approves, go to
    # the human review gate (which is a no-op for non-sensitive runs).
    graph.add_conditional_edges(
        "critic",
        lambda s: "human_review" if s.get("approved", False) else critic_router(s),
        {"writer": "writer", "human_review": "human_review", "end": END},
    )

    # After the human review, either end or loop back to the writer.
    graph.add_conditional_edges(
        "human_review",
        human_router,
        {"writer": "writer", "end": END},
    )

    # Checkpointer is required for interrupt() to work.
    return graph.compile(checkpointer=MemorySaver())


def run_research_with_hitl(question: str, max_iterations: int = 2) -> State:
    """Run the HITL-enabled graph. If interrupt() fires, ask the human and
    resume with Command(resume=...)."""
    team = select_team(question)
    console.print(f"\nSELECTED TEAM ({len(team)} agents): {', '.join(team)}\n")

    initial_state: State = {
        "question": question,
        "team": team,
        "queries": [],
        "sources": [],
        "facts": [],
        "disclaimer": "",
        "answer": "",
        "critic_feedback": "",
        "approved": False,
        "issues": [],
        "iterations": 0,
        "max_iterations": max_iterations,
        "sources_count": 0,
        "llm_calls": 0,
        "duration_seconds": 0.0,
        "was_sensitive": is_sensitive(question),
        "critic_first_pass": False,
        "final_answer": "",
    }

    graph = build_research_graph_hitl()
    config = {"configurable": {"thread_id": str(uuid.uuid4())}, "recursion_limit": 50}
    start_time = time.time()

    # First invocation - runs until the graph either finishes or hits interrupt().
    result = graph.invoke(initial_state, config=config)

    # Loop while the graph is paused on a human-review interrupt.
    while True:
        snapshot = graph.get_state(config)
        # If there are no more pending tasks, the graph is done.
        if not snapshot.next:
            break

        # Pull the interrupt payload (the dict passed to interrupt()).
        interrupts = []
        for task in snapshot.tasks:
            interrupts.extend(task.interrupts)
        if not interrupts:
            break

        # Ask the human. Fallback to auto-approve in non-interactive contexts
        # (same shape as the vanilla EOFError fallback).
        try:
            human_decision = input("Approve this draft? (y / reason): ").strip()
        except EOFError:
            console.print("[dim](non-interactive - defaulting to approve)[/dim]")
            human_decision = "y"

        if human_decision.lower() != "y" and human_decision != "":
            # Treat as a rejection - pass the reason through.
            result = graph.invoke(Command(resume=human_decision), config=config)
        else:
            result = graph.invoke(Command(resume="y"), config=config)

    result["duration_seconds"] = time.time() - start_time
    result["final_answer"] = result.get("answer", "")
    return result


"""## Writer Agent: Turn Findings Into A Usable Answer

The Writer is the synthesis role. It is where the system turns retrieval, reading, and compliance guidance into something a user can consume.

The Writer is not supposed to invent the whole answer from scratch. Ideally, it should draw on the facts, the sources, and any required disclaimer.

This is another useful point for class discussion:

- a system can have good retrieval but poor synthesis,
- or strong synthesis but weak evidence,
- and both cases can produce misleadingly polished answers.

That is one reason the Critic role exists after the Writer.
"""

def writer_agent(
    question: str,
    sources: list[Source],
    facts: list[str],
    disclaimer: str,
    critic_feedback: str = "",
) -> str:
    sources_text = "\n".join(
        f"- {source['title']} ({source['url']})" for source in sources[:8]
    )
    facts_text = "\n".join(f"- {fact}" for fact in facts) if facts else "(none)"
    feedback_text = ""
    if critic_feedback:
        feedback_text = (
            "\n\nPREVIOUS CRITIC FEEDBACK TO ADDRESS:\n"
            f"{critic_feedback}"
        )

    disclaimer_line = (
        f"Include this disclaimer at the end: {disclaimer}" if disclaimer else ""
    )
    prompt = f"""Write a clear, well-structured answer (150-300 words) to the question.
Use the facts and cite sources where appropriate.
{disclaimer_line}

Question: {question}

Facts:
{facts_text}

Sources:
{sources_text}{feedback_text}"""
    return call_llm(prompt, max_tokens=600)


"""## RACI: Who Owns What?

Once we know the team, the next question is: **who is doing what, and who is accountable for the result?**

This is exactly the question RACI was invented to answer. RACI stands for:

- **R**esponsible — who actually does the work,
- **A**ccountable — who is on the hook for whether it was done well,
- **C**onsulted — whose input was used,
- **I**nformed — who needs to know what happened.

In this notebook, the Critic is the role that owns quality. So the Critic is **Accountable** for most tasks, even though the Critic does not produce the work itself.

That distinction (R vs A) is the most useful piece of RACI for multi-agent systems. The agent that *produces* an artifact is rarely the same as the agent that *owns* its quality.
"""

def build_raci(team: list[str]) -> list[RACIEntry]:
    entries: list[RACIEntry] = []

    if "Planner" in team:
        entries.append(
            RACIEntry(task="Plan sub-queries", responsible="Planner", accountable="Critic")
        )
    if "Searcher" in team:
        entries.append(
            RACIEntry(
                task="Find sources",
                responsible="Searcher",
                accountable="Critic",
                consulted="Planner",
            )
        )
    if "Reader" in team:
        entries.append(
            RACIEntry(
                task="Extract facts",
                responsible="Reader",
                accountable="Critic",
                consulted="Searcher",
            )
        )
    if "Compliance" in team:
        entries.append(
            RACIEntry(
                task="Add disclaimer",
                responsible="Compliance",
                accountable="Critic",
            )
        )
    if "Writer" in team:
        entries.append(
            RACIEntry(
                task="Write answer",
                responsible="Writer",
                accountable="Critic",
                consulted="Searcher",
            )
        )
    if "Critic" in team:
        entries.append(
            RACIEntry(task="Verify answer", responsible="-", accountable="Critic")
        )

    return entries



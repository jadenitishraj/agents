"""## Shared LLM Wrapper

Several agents call the language model. Instead of repeating that code in every agent, we use one shared wrapper.

This is not about production complexity. It simply keeps the notebook easier to read:

- one place to set the model,
- one place to define temperature and token limits,
- one function that the agents can reuse.

Notice the conceptual split:

- the taxonomy defines **who** exists,
- the wrapper defines **how** those roles talk to the LLM.

### What changed from the vanilla version

The vanilla notebook called the OpenAI client directly. We now use `ChatOpenAI` from `langchain_openai`. The interface is essentially the same — `prompt -> string` — but it lets every agent in the graph share one consistent LLM client and makes it trivial to swap providers later.
"""

from langchain_openai import ChatOpenAI
from langchain_core.messages import HumanMessage

_llm: ChatOpenAI | None = None


def get_llm(max_tokens: int = 500) -> ChatOpenAI:
    """Lazily build a ChatOpenAI client. We keep one instance per token budget
    so the model stays consistent across the notebook."""
    global _llm
    if _llm is None:
        _llm = ChatOpenAI(model="gpt-4o-mini", temperature=0.3)
    return _llm


def call_llm(prompt: str, max_tokens: int = 500) -> str:
    """Send a single user prompt and return the text reply. Same shape as the
    vanilla wrapper - one prompt in, one string out."""
    llm = get_llm(max_tokens=max_tokens).bind(max_tokens=max_tokens)
    response = llm.invoke([HumanMessage(content=prompt)])
    return response.content.strip()

"""## Planner Agent: Break One Big Question Into Smaller Search Tasks

The Planner is the decomposition role.

Its job is not to answer the question directly. Its job is to make the next step easier by turning one broad question into several narrower search queries.

This is a useful design pattern in multi-agent systems because retrieval quality often depends on query quality. If the search queries are vague, the entire downstream pipeline becomes weaker.

So the Planner adds value by making the search space more explicit.
"""

def planner_agent(question: str) -> list[str]:
    prompt = f"""Break this research question into 3 to 5 specific web search queries.
Return ONLY the queries, one per line, no numbering or extra text.

Question: {question}"""
    text = call_llm(prompt, max_tokens=200)
    queries = [line.strip() for line in text.splitlines() if line.strip()]
    return queries[:5]


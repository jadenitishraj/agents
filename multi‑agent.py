"""## Taxonomy: What Roles Exist?

A **taxonomy** is a classification of roles. In this notebook, taxonomy does not mean hierarchy, seniority, or an org chart. It means: **what kinds of workers exist in this system, and what distinct mission does each one have?**

This separation matters because a multi-agent system is easier to reason about when each role has a focused job. If one agent tries to plan, search, read, write, review, and manage compliance all at once, the teaching value drops and the control structure becomes blurry.

So the taxonomy gives us a menu of possible capabilities:

- Planner breaks a question into sub-queries.
- Searcher gathers evidence.
- Reader extracts facts from that evidence.
- Writer turns findings into an answer.
- Critic evaluates whether the answer is good enough.
- Compliance adds extra safeguards in sensitive domains.

The taxonomy answers the question: **"What roles are available in this system?"**

### From `RunTrace` (dataclass) to `State` (TypedDict)

In the vanilla version we tracked everything in a `RunTrace` dataclass and threaded it manually through a `for` loop. In LangGraph, the framework already gives us a first-class concept for this: **state**.

The `State` is a `TypedDict` that flows through every node in the graph. Each node receives the current state, does its work, and returns the fields it wants to update. LangGraph merges those updates back into the state automatically.

So conceptually:

- `RunTrace` (vanilla) → `State` (LangGraph) — same fields, different container.
- The orchestrator's `for` loop → a `StateGraph` with conditional edges.
- The manual feedback threading → state mutation on each node.

`RACIEntry` and `IncentiveRisk` stay as dataclasses because they are *outputs* (lookup tables and scan reports), not flowing state. Only the running pipeline data lives in `State`.
"""


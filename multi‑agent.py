"""## Reader Agent: Turn Raw Retrieval Into Explicit Facts

The Searcher gathers evidence, but it does not organize that evidence into usable findings. That is the Reader's job.

The Reader is useful when the question is deep, comparative, or detail-heavy. It reads across source snippets and extracts a smaller set of facts that the Writer can later use.

Conceptually, the Reader helps the system move from **data collection** to **evidence interpretation**.
"""

def reader_agent(question: str, sources: list[Source]) -> list[str]:
    snippets = "\n\n".join(
        f"[{index + 1}] {source['title']}\n{source['snippet']}"
        for index, source in enumerate(sources[:8])
    )
    prompt = f"""Read these source snippets and extract 5-10 key facts relevant to the question.
Return ONLY the facts, one per line, no numbering.

Question: {question}

Sources:
{snippets}"""
    text = call_llm(prompt, max_tokens=600)
    return [line.strip() for line in text.splitlines() if line.strip()]


"""## Searcher Agent: Gather External Evidence

The Searcher is the retrieval role. It uses DuckDuckGo to gather sources from the open web.

This is the first place where the system leaves its own internal reasoning and touches outside evidence. That makes the Searcher important for both quality and risk:

- strong retrieval can improve factual grounding,
- weak retrieval can poison everything downstream,
- too few sources can make the final answer look polished but thinly supported.

In this notebook, the Searcher returns a simple structure for each source: title, URL, and snippet.
"""

def searcher_agent(queries: list[str]) -> list[Source]:
    try:
        from ddgs import DDGS
    except ModuleNotFoundError as error:
        raise RuntimeError(
            "Searcher agent requires ddgs. Run the setup cell first."
        ) from error

    sources: list[Source] = []
    seen_urls: set[str] = set()

    with DDGS() as ddgs:
        for query in queries:
            try:
                results = list(ddgs.text(query, max_results=3))
                for result in results:
                    url = result.get("href", "")
                    if url and url not in seen_urls:
                        seen_urls.add(url)
                        sources.append(
                            {
                                "title": result.get("title", ""),
                                "url": url,
                                "snippet": result.get("body", ""),
                            }
                        )
            except Exception as error:
                console.print(f"   Search failed for '{query}': {error}")
                continue

    return sources

